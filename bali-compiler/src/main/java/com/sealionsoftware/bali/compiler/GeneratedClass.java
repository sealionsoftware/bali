public class GeneratedClass {

	private String name;
	private byte[] code;

	public GeneratedClass(String name, byte[] code) {
		this.name = name;
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public byte[] getCode() {
		return code;
	}

}
